function dateAt(dateStr: string): string {
  return new Date(dateStr + "T00:10:00.000Z").toISOString();
}

function scheduledAt(daysFromNow: number): string {
  const d = new Date();
  d.setDate(d.getDate() + daysFromNow);
  d.setHours(0, 10, 0, 0);
  return d.toISOString();
}

