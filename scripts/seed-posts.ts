async function seed() {
  console.log(`총 ${POSTS.length}개 포스트 삽입 시작...`);

  let success = 0;
  let skip = 0;
  let fail = 0;

  for (const post of POSTS) {
    try {
      await db.execute({
        sql: `INSERT OR IGNORE INTO posts (type, slug, title, summary, content, keywords, status, tool_id, published_at)
              VALUES (?, ?, ?, ?, ?, ?, 'scheduled', ?, ?)`,
        args: [
          post.type,
          post.slug,
          post.title,
          post.summary,
          post.content,
          JSON.stringify(post.keywords),
          post.tool_id ?? null,
          post.published_at,
        ],
      });
      success++;
      console.log(`  ✓ [${post.published_at.slice(0, 10)}] ${post.title}`);
    } catch (err) {
      const msg = err instanceof Error ? err.message : String(err);
      if (msg.includes("UNIQUE constraint failed")) {
        skip++;
        console.log(`  - SKIP (이미 존재): ${post.slug}`);
      } else {
        fail++;
        console.error(`  ✗ FAIL: ${post.slug} — ${msg}`);
      }
    }
  }

  console.log(`\n완료: 성공 ${success}개 / 스킵 ${skip}개 / 실패 ${fail}개`);
  console.log(
    `공개 일정: ${POSTS[0].published_at.slice(0, 10)} ~ ${POSTS[POSTS.length - 1].published_at.slice(0, 10)}`,
  );
}

seed().catch(console.error);
