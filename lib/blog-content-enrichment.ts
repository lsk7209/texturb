export function getBlogContentEnrichment(slug: string): BlogContentEnrichment | undefined {
  return BLOG_CONTENT_ENRICHMENTS.find((item) => item.slug === slug)
}

export function getEnrichedBlogContent(slug: string, content: string): string {
  const enrichment = getBlogContentEnrichment(slug)
  return enrichment ? `${content}${enrichment.content}` : content
}
